/**
 * Search the local plant database by name or scientific name.
 */
export function searchPlantDatabase(query: string): PlantDatabaseEntry[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return plantDatabase.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.scientificName.toLowerCase().includes(q) ||
      p.family.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q)
  );
}

/**
 * Get a plant by its ID.
 */
export function getPlantByDatabaseId(id: string): PlantDatabaseEntry | undefined {
  return plantDatabase.find((p) => p.id === id);
}

/**
 * Get all plants in a category.
 */
export function getPlantsByCategory(category: PlantDatabaseEntry['category']): PlantDatabaseEntry[] {
  return plantDatabase.filter((p) => p.category === category);
}

/**
 * Check if a plant is in the database by name.
 */
export function findPlantByName(name: string): PlantDatabaseEntry | undefined {
  const q = name.toLowerCase().trim();
  return plantDatabase.find(
    (p) => p.name.toLowerCase() === q || p.scientificName.toLowerCase() === q
  );
}
